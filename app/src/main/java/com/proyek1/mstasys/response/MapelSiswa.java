    public void setId_kategori(String id_kategori) {
        this.id_kategori = id_kategori;
    }

    public String getKategori_mapel() {
        return kategori_mapel;
    }

    public void setKategori_mapel(String kategori_mapel) {
        this.kategori_mapel = kategori_mapel;
    }
